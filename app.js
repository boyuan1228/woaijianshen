function backupData() {
  const payload = {
    version: 2,
    exportedAt: new Date().toISOString(),
    profile: state.profile,
    survey: state.survey,
    logs: state.logs,
  };
  const blob = new Blob([JSON.stringify(payload, null, 2)], { type: "application/json;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `rhts-sstt-backup-${new Date().toISOString().slice(0, 10)}.json`;
  link.click();
  URL.revokeObjectURL(url);
}

function restoreData(file) {
  if (!file) return;
  const reader = new FileReader();
  reader.onload = () => {
    try {
      const payload = JSON.parse(reader.result);
      if (!payload.profile || !payload.survey) throw new Error("Invalid backup");
      Object.assign(state.profile, payload.profile);
      Object.assign(state.survey, payload.survey);
      state.logs = payload.logs || {};
      state.weekIndex = 0;
      state.dayIndex = 0;
      saveState();
      render();
      alert("数据已恢复。");
    } catch {
      alert("无法读取这个备份文件。");
    }
  };
  reader.readAsText(file);
}

function resetLogs() {
  if (!confirm("清空所有本地训练记录？训练计划和问卷不会被删除。")) return;
  state.logs = {};
  saveState();
  render();
}

