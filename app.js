function averageGap(capacities, phase) {
  const gaps = Object.values(capacities).map((lift) => lift.phases[phase]?.gap || 0);
  return gaps.reduce((sum, value) => sum + value, 0) / gaps.length;
}

function mesoPattern(gap) {
  if (gap >= 8) return { overload: 4, deload: 1 };
  if (gap >= 5) return { overload: 3, deload: 1 };
  return { overload: 2, deload: 1 };
}

function phaseWeekText(phase, capacities) {
  if (!["hypertrophy", "strength", "peaking"].includes(phase.key)) {
    return `${phase.weeks} 周：${phase.note}`;
  }
  const pattern = mesoPattern(averageGap(capacities, phase.key));
  const blocks = [];
  let remaining = phase.weeks;
  while (remaining > 0) {
    const overload = Math.min(pattern.overload, remaining);
    remaining -= overload;
    const deload = remaining > 0 ? Math.min(pattern.deload, remaining) : 0;
    remaining -= deload;
    blocks.push(`${overload} 周超负荷${deload ? ` + ${deload} 周减载` : ""}`);
  }
  return `${phase.weeks} 周：${blocks.join("，")}。${phase.note}`;
}

