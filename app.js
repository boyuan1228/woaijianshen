function rawFrequencyFor(lift, capacity) {
  const mid = (capacity.phases.strength.mev + capacity.phases.strength.mrv) / 2;
  if (lift === "bench") {
    if (mid <= 10) return 2;
    if (mid <= 14) return 2.5;
    if (mid <= 18) return 3;
    return 4;
  }
  if (lift === "squat") {
    if (mid <= 8) return 1.5;
    if (mid <= 12) return 2;
    return 2.5;
  }
  if (mid <= 5) return 1;
  if (mid <= 8) return 1.5;
  return 2;
}

function weeklyTrainingDays(capacities, frequencies) {
  const strengthMrvs = Object.values(capacities).map((capacity) => capacity.phases.strength.mrv);
  const allMrvs = Object.values(capacities).flatMap((capacity) =>
    Object.values(capacity.phases).map((phase) => phase.mrv)
  );
  const avgMrv = strengthMrvs.reduce((sum, value) => sum + value, 0) / strengthMrvs.length;
  const maxMrv = Math.max(...allMrvs);
  const totalFrequency = Object.values(frequencies).reduce((sum, value) => sum + value, 0);

  let days = 3;
  if (totalFrequency > 5.5 || avgMrv >= 12) days = 4;
  if (totalFrequency > 7.5 || avgMrv >= 16) days = 5;
  if (totalFrequency > 9 || avgMrv >= 19) days = 6;

  if (maxMrv >= 16) days = Math.max(days, 4);
  if (maxMrv >= 21) days = Math.max(days, 5);
  return clamp(days, 3, 6);
}

function frequencyFor(lift, capacity, weeklyDays) {
  const raw = rawFrequencyFor(lift, capacity);
  if (lift === "bench") return Math.min(raw, weeklyDays);
  return Math.min(raw, Math.max(1, weeklyDays - 1));
}

function makeFrequencies(capacities) {
  const raw = Object.fromEntries(
    Object.entries(capacities).map(([lift, capacity]) => [lift, rawFrequencyFor(lift, capacity)])
  );
  const recommendedDays = weeklyTrainingDays(capacities, raw);
  const requestedDays = state.survey.trainingDays === "auto" ? recommendedDays : Number(state.survey.trainingDays);
  const days = clamp(requestedDays || recommendedDays, 3, 6);
  const frequencies = Object.fromEntries(
    Object.entries(capacities).map(([lift, capacity]) => [lift, frequencyFor(lift, capacity, days)])
  );
  return { days, recommendedDays, frequencies };
}

