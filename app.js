function pdfPlanWeeks() {
  const savedWeek = state.weekIndex;
  const savedDay = state.dayIndex;
  const plan = makePlanner();
  const weeks = [];

  planWeeks(plan).forEach((week, weekIndex) => {
    const status = weekStatusFor(weekIndex, plan);
    const days = week.days.map((day, dayIndex) => {
      state.weekIndex = weekIndex;
      state.dayIndex = dayIndex;
      const log = state.logs[logKey(weekIndex, dayIndex)] || { itemNotes: {}, backdowns: {}, accessories: {} };
      const rows = [];
      day.items.forEach((item, itemIndex) => {
        if (isBackdownRow(item)) {
          generatedBackdownRows(item, itemIndex, log, day.items).forEach((row) => {
            rows.push([
              `${displayName(item)} · ${localizeBackdownStatus(row.status)}`,
              row.sets,
              row.reps,
              row.rpe,
              formatLoadText(row.load),
              "",
            ]);
          });
          return;
        }

        rows.push([
          displayNameForRow(item, itemIndex, log, day.items),
          item.sets || "-",
          item.reps || "-",
          item.rpe || "-",
          formatLoadText(estimatedLoadWithContext(item, itemIndex, day.items) || item.weight || "-"),
          noteForItem(day.items, itemIndex, log),
        ]);

        expandedSetRows(item).forEach((row) => {
          rows.push([
            `${displayName(item)} · ${row.label}`,
            row.sets,
            row.reps,
            row.rpe,
            formatLoadText(row.load),
            "",
          ]);
        });
      });
      return { day: day.day, rows };
    });
    weeks.push({ week, status, days });
  });

  state.weekIndex = savedWeek;
  state.dayIndex = savedDay;
  return weeks;
}

