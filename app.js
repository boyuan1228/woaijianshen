function testWeekLoad(item, index, items) {
  if (!isTestWeekIndex()) return "";
  const type = movementType(item);
  if (!["squat", "bench", "deadlift"].includes(type) || isBackdownRow(item)) return "";
  const day = currentDay().day.toLowerCase();
  const occurrence = movementOccurrence(items, index);
  const max = currentMainMax(type);
  const opener = openingAttempt(type);
  let load = 0;

  if (day.includes("sun") && type === "bench") {
    if (occurrence === 1 && opener) load = opener * 0.975;
    if (occurrence === 2 && max) load = max * 0.84;
  }
  if (day.includes("tue")) {
    if (type === "squat") {
      if (occurrence === 1 && opener) load = opener * 0.95;
      if (occurrence === 2 && max) load = max * 0.786;
    }
    if (type === "deadlift") {
      if (occurrence === 1 && opener) load = opener * 0.95;
      if (occurrence === 2 && max) load = max * 0.811;
    }
  }
  if (day.includes("wed") && type === "bench") {
    if (occurrence === 1 && max) load = max * 0.85;
    if (occurrence === 2 && max) load = max * 0.725;
  }
  if (day.includes("sat") && opener) load = opener;

  return load ? `${roundLoad(load)} kg` : "";
}

