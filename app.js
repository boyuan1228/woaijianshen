function capacityAdjustment(lift) {
  return adjustmentRules(lift).reduce(
    (sum, rule) => ({ mev: sum.mev + rule.mev, mrv: sum.mrv + rule.mrv }),
    { mev: 0, mrv: 0 }
  );
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function makeCapacities() {
  const base = {
    squat: { name: "深蹲", hypertrophy: [8, 16], strength: [6, 12], peaking: [3, 8] },
    bench: { name: "卧推", hypertrophy: [10, 20], strength: [8, 16], peaking: [4, 10] },
    deadlift: { name: "硬拉", hypertrophy: [5, 10], strength: [4, 8], peaking: [2, 6] },
  };

  return Object.fromEntries(
    Object.entries(base).map(([lift, item]) => {
      const adj = capacityAdjustment(lift);
      const phases = {};
      ["hypertrophy", "strength", "peaking"].forEach((phase) => {
        const [baseMev, baseMrv] = item[phase];
        const mev = clamp(baseMev + adj.mev, 1, 24);
        const mrv = clamp(baseMrv + adj.mrv, mev + 2, 30);
        phases[phase] = { mev, mrv, gap: mrv - mev };
      });
      return [lift, { name: item.name, phases }];
    })
  );
}

function allocatePhaseWeeks(totalWeeks) {
  const { beginnerLightFemale, advancedHeavyMale } = athleteProfileScore();
  const transition = totalWeeks >= 10 ? 1 : 0;
  const bridge = totalWeeks >= 12 ? 1 : 0;
  const available = Math.max(6, totalWeeks - transition - bridge);

  const growthPct = clamp(0.2 + beginnerLightFemale * 0.035 - advancedHeavyMale * 0.025, 0, 0.5);
  const strengthPct = clamp(0.42 + advancedHeavyMale * 0.035 - beginnerLightFemale * 0.015, 0.2, 0.7);
  const peakingPct = clamp(1 - growthPct - strengthPct, 0.15, 0.5);
  const normalized = growthPct + strengthPct + peakingPct;

  let hypertrophy = Math.round((growthPct / normalized) * available);
  let strength = Math.round((strengthPct / normalized) * available);
  let peaking = available - hypertrophy - strength;

  hypertrophy = growthPct === 0 ? 0 : clamp(hypertrophy, 3, Math.min(16, available));
  strength = clamp(strength, 3, Math.min(16, available - hypertrophy));
  peaking = clamp(available - hypertrophy - strength, 2, Math.min(8, available));
  strength = available - hypertrophy - peaking;

  return [
    { key: "transition", name: "过渡期", weeks: transition, note: "从当前训练过渡到计划起点，保留技术练习，降低疲劳。" },
    { key: "hypertrophy", name: "增肌期", weeks: hypertrophy, note: "用较高容量建立肌肉量与技术容量。" },
    { key: "strength", name: "增力期", weeks: strength, note: "提高专项强度，容量保持在 MEV 到 MRV 之间推进。" },
    { key: "peaking", name: "冲刺期", weeks: peaking, note: "降低总量，提高专项强度和单次表现。" },
    { key: "bridge", name: "桥接期", weeks: bridge, note: "比赛后或进入下一周期前恢复、评估并重新设定训练最大值。" },
  ].filter((phase) => phase.weeks > 0);
}

function averageGap(capacities, phase) {
  const gaps = Object.values(capacities).map((lift) => lift.phases[phase]?.gap || 0);
  return gaps.reduce((sum, value) => sum + value, 0) / gaps.length;
}

function mesoPattern(gap) {
  if (gap >= 8) return { overload: 4, deload: 1 };
  if (gap >= 5) return { overload: 3, deload: 1 };
  return { overload: 2, deload: 1 };
}

function phaseWeekText(phase, capacities) {
  if (!["hypertrophy", "strength", "peaking"].includes(phase.key)) {
    return `${phase.weeks} 周：${phase.note}`;
  }
  const pattern = mesoPattern(averageGap(capacities, phase.key));
  const blocks = [];
  let remaining = phase.weeks;
  while (remaining > 0) {
    const overload = Math.min(pattern.overload, remaining);
    remaining -= overload;
    const deload = remaining > 0 ? Math.min(pattern.deload, remaining) : 0;
    remaining -= deload;
    blocks.push(`${overload} 周超负荷${deload ? ` + ${deload} 周减载` : ""}`);
  }
  return `${phase.weeks} 周：${blocks.join("，")}。${phase.note}`;
}

