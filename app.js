function makePlanner() {
  const capacities = makeCapacities();
  const totalWeeks = weeksUntilMeet();
  const systemKey = state.survey.programSystem || "jtsSstt";
  if (systemKey !== "jtsSstt") {
    const recommendedDays = recommendedDaysForSystem(systemKey);
    const requestedDays = state.survey.trainingDays === "auto" ? recommendedDays : Number(state.survey.trainingDays);
    const days = clamp(requestedDays || recommendedDays, 3, 6);
    const phases = systemKey === "jts" ? allocatePhaseWeeks(totalWeeks) : allocateSystemPhaseWeeks(totalWeeks, systemKey);
    const frequencies = systemKey === "jts" ? makeFrequencies(capacities).frequencies : systemFrequencies(days, systemKey);
    const weeklyLayout = makeWeeklyLayout(days, frequencies, systemKey === "jts" ? "linear" : systemKey);
    return {
      capacities,
      totalWeeks,
      phases,
      frequencies,
      days,
      recommendedDays,
      weeklyLayout,
      systemKey,
      usesJtsVolume: systemKey === "jts",
    };
  }
  const phases = allocatePhaseWeeks(totalWeeks);
  const { days, recommendedDays, frequencies } = makeFrequencies(capacities);
  const weeklyLayout = makeWeeklyLayout(days, frequencies, modelForPhase("strength"));
  return { capacities, totalWeeks, phases, frequencies, days, recommendedDays, weeklyLayout, systemKey, usesJtsVolume: true };
}

function phaseForWeek(weekNumber, phases) {
  let cursor = 0;
  for (const phase of phases) {
    cursor += phase.weeks;
    if (weekNumber <= cursor) return phase;
  }
  return phases[phases.length - 1];
}

function phaseProgress(phase, localWeek, capacities) {
  if (!["hypertrophy", "strength", "peaking"].includes(phase.key)) return "恢复/桥接";
  const pattern = mesoPattern(averageGap(capacities, phase.key));
  const cycleLength = pattern.overload + pattern.deload;
  const pos = ((localWeek - 1) % cycleLength) + 1;
  return pos > pattern.overload ? "减载周" : `超负荷 ${pos}/${pattern.overload}`;
}

function weekStatusFor(index, plan) {
  if (index === plan.totalWeeks - 1) {
    return { phase: { key: "test", name: "测试周" }, progress: "比赛/测试", isDeload: false };
  }
  const phase = phaseForWeek(index + 1, plan.phases);
  let localWeek = index + 1;
  let cursor = 0;
  for (const item of plan.phases) {
    if (item === phase) {
      localWeek = index + 1 - cursor;
      break;
    }
    cursor += item.weeks;
  }
  const progress = phaseProgress(phase, localWeek, plan.capacities);
  return { phase, localWeek, progress, isDeload: progress.includes("减载") };
}

function recommendedWeeklySets(lift, phaseKey, localWeek, phaseWeeks, capacities) {
  const cap = capacities[lift].phases[phaseKey];
  if (!cap) return "-";
  const ratio = phaseWeeks <= 1 ? 0 : (localWeek - 1) / Math.max(1, phaseWeeks - 1);
  if (phaseKey === "peaking") return Math.round(cap.mrv - ratio * (cap.mrv - cap.mev));
  return Math.round(cap.mev + ratio * (cap.mrv - cap.mev));
}

function completionForWeek(weekIndex) {
  const week = virtualWeek(weekIndex);
  let total = 0;
  let done = 0;
  week.days.forEach((day, dayIndex) => {
    const log = state.logs[logKey(weekIndex, dayIndex)];
    total += day.items.length;
    done += log?.done?.filter(Boolean).length || 0;
  });
  return total ? Math.round((done / total) * 100) : 0;
}

