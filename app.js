function normalizeActivationCode(value) {
  return String(value || "").trim().toUpperCase().replace(/\s+/g, "");
}

async function sha256Hex(value) {
  if (!window.crypto?.subtle) {
    throw new Error("当前浏览器不支持本地激活校验，请换 Chrome/Edge 或接入远程校验接口。");
  }
  const bytes = new TextEncoder().encode(value);
  const hash = await window.crypto.subtle.digest("SHA-256", bytes);
  return Array.from(new Uint8Array(hash))
    .map((byte) => byte.toString(16).padStart(2, "0"))
    .join("");
}

async function verifyActivationCode(code) {
  const normalized = normalizeActivationCode(code);
  if (!normalized) return { ok: false, message: "请输入激活码。" };

  if (LICENSE_VERIFY_ENDPOINT) {
    const response = await fetch(LICENSE_VERIFY_ENDPOINT, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ code: normalized }),
    });
    if (!response.ok) return { ok: false, message: "激活服务暂时不可用，请稍后重试。" };
    const payload = await response.json();
    return {
      ok: Boolean(payload.active),
      hash: payload.licenseId || (await sha256Hex(normalized)),
      message: payload.message || "激活码无效。",
    };
  }

  const hash = await sha256Hex(normalized);
  const ok = normalized === "WOAIJIANSHEN" && LOCAL_LICENSE_HASHES.has(hash);
  return {
    ok,
    hash,
    message: ok ? "激活成功。" : "激活码不正确，请输入 woaijianshen。",
  };
}

