function ageFromBirthDate() {
  if (state.survey.birthYear) {
    const month = Number(state.survey.birthMonth || 1) - 1;
    const birth = new Date(Number(state.survey.birthYear), month, 1, 12);
    let age = today.getFullYear() - birth.getFullYear();
    if (today.getMonth() < birth.getMonth()) age -= 1;
    return Math.max(10, age);
  }
  if (!state.survey.birthDate) return 28;
  const dateText = state.survey.birthDate.length === 7 ? `${state.survey.birthDate}-01` : state.survey.birthDate;
  const birth = new Date(`${dateText}T12:00:00`);
  if (Number.isNaN(birth.getTime())) return 28;
  let age = today.getFullYear() - birth.getFullYear();
  if (
    today.getMonth() < birth.getMonth() ||
    (today.getMonth() === birth.getMonth() && today.getDate() < birth.getDate())
  ) {
    age -= 1;
  }
  return Math.max(10, age);
}

function bodyweightClass() {
  const bw = bodyweightKg();
  const female = state.survey.sex === "female";
  if (!bw) return "普通体重";
  if (female) {
    if (bw < 57) return "轻体重";
    if (bw <= 75) return "普通体重";
    if (bw <= 90) return "重量级";
    return "超重量级";
  }
  if (bw < 75) return "轻体重";
  if (bw <= 100) return "普通体重";
  if (bw <= 125) return "重量级";
  return "超重量级";
}

function heightClass() {
  const height = Number(state.survey.height || 0);
  const female = state.survey.sex === "female";
  if (!height) return "中等身高";
  if (female) {
    if (height < 160) return "矮小";
    if (height <= 167) return "中等身高";
    if (height <= 175) return "高大";
    return "非常高大";
  }
  if (height < 170) return "矮小";
  if (height <= 182) return "中等身高";
  if (height <= 195) return "高大";
  return "非常高大";
}

function weeksUntilMeet() {
  if (!state.survey.meetDate) return DEFAULT_PLAN_WEEKS;
  const meet = new Date(`${state.survey.meetDate}T12:00:00`);
  if (Number.isNaN(meet.getTime())) return DEFAULT_PLAN_WEEKS;
  const ms = meet.getTime() - today.getTime();
  if (ms <= 0) return 6;
  return Math.max(6, Math.round(ms / (7 * 24 * 60 * 60 * 1000)));
}

function levelScore(lift) {
  return { low: 0, medium: 1, high: 2, veryHigh: 3 }[state.survey[`${lift}Level`]] || 0;
}

function athleteProfileScore() {
  const bw = bodyweightKg();
  let beginnerLightFemale = 0;
  let advancedHeavyMale = 0;

  if (state.survey.sex === "female") beginnerLightFemale += 2;
  if (state.survey.sex === "male") advancedHeavyMale += 2;
  if (bw && bw < 75) beginnerLightFemale += 1;
  if (bw && bw >= 100) advancedHeavyMale += 1;
  if (state.survey.experience === "beginner") beginnerLightFemale += 2;
  if (["advanced", "veryAdvanced"].includes(state.survey.experience)) advancedHeavyMale += 2;
  advancedHeavyMale += (levelScore("squat") + levelScore("bench") + levelScore("deadlift")) / 2;

  return { beginnerLightFemale, advancedHeavyMale };
}

