async function activateFromInput() {
  const input = $("activationCodeInput");
  const button = $("activationButton");
  const message = $("activationMessage");
  message.textContent = "";
  message.classList.remove("ok");
  button.disabled = true;
  button.textContent = "校验中...";
  try {
    const result = await verifyActivationCode(input.value);
    if (!result.ok) {
      message.textContent = result.message;
      return;
    }
    saveLicense(result.hash);
    message.textContent = "激活成功，正在进入计划生成器。";
    message.classList.add("ok");
    renderActivationGate();
    render();
  } catch (error) {
    message.textContent = error.message || "激活失败，请稍后重试。";
  } finally {
    button.disabled = false;
    button.innerHTML = `<i data-lucide="key-round"></i>激活并进入`;
    if (window.lucide) window.lucide.createIcons();
  }
}

