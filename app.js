function ageFromBirthDate() {
  if (state.survey.birthYear) {
    const month = Number(state.survey.birthMonth || 1) - 1;
    const birth = new Date(Number(state.survey.birthYear), month, 1, 12);
    let age = today.getFullYear() - birth.getFullYear();
    if (today.getMonth() < birth.getMonth()) age -= 1;
    return Math.max(10, age);
  }
  if (!state.survey.birthDate) return 28;
  const dateText = state.survey.birthDate.length === 7 ? `${state.survey.birthDate}-01` : state.survey.birthDate;
  const birth = new Date(`${dateText}T12:00:00`);
  if (Number.isNaN(birth.getTime())) return 28;
  let age = today.getFullYear() - birth.getFullYear();
  if (
    today.getMonth() < birth.getMonth() ||
    (today.getMonth() === birth.getMonth() && today.getDate() < birth.getDate())
  ) {
    age -= 1;
  }
  return Math.max(10, age);
}

