function rpePercent(reps, rpe) {
  const roundedRpe = Math.round(Number(rpe) * 2) / 2;
  const row = RPE_PERCENT_TABLE[roundedRpe];
  const repIndex = Math.min(Math.max(Math.round(Number(reps)), 1), 10) - 1;
  return row?.[repIndex] || 0;
}

function estimatedLoad(item) {
  const type = movementType(item);
  const maxKey = {
    bench: "bench",
    squat: "squat",
    deadlift: "deadlift",
    benchVariant: "benchVariantMax",
    squatVariant: "squatVariantMax",
    deadliftVariant: "deadliftVariantMax",
  }[type];
  if (!maxKey) return "";
  const oneRm = Number(state.profile[maxKey] || 0);
  const reps = numberFrom(item.reps);
  const rpe = primaryRpe(item.rpe) || implicitMainVolumeRpe(item);
  if (!oneRm || !reps || !rpe || String(item.sets) === "0") return "";

  const percent = rpePercent(reps, rpe);
  if (!percent) return "";
  const load = roundLoad(oneRm * percent);
  return load > 0 ? `${load} kg` : "";
}

