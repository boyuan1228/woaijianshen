function backdownGoal(item) {
  const type = movementType(item);
  const lift = type.replace("Variant", "");
  if (!["bench", "squat", "deadlift"].includes(lift)) return 0;
  const plan = makePlanner();
  const phase = phaseKeyForCurrentWeek();
  const cap = plan.capacities[lift]?.phases[phase] || plan.capacities[lift]?.phases.strength;
  const freq = plan.frequencies[lift] || 2;
  if (!cap) return 4;
  return clamp(Math.round(((cap.mev + cap.mrv) / 2) / freq) - 1, 1, 6);
}

function parsedSetPrescription(item, index, log) {
  const raw = String(log.backdowns[index] || "").trim();
  const reps = item.reps || "";
  if (!raw) return { sets: 0, reps, text: `0x${reps}` };
  const match = raw.match(/^(\d+)\s*(?:x|×|\*)?\s*(\d+)?$/i);
  const sets = match ? Number(match[1]) : Number(raw) || 0;
  const parsedReps = match?.[2] || reps;
  return { sets, reps: parsedReps, text: `${sets}x${parsedReps}` };
}

function topSetLoadBefore(items, index) {
  for (let cursor = index - 1; cursor >= 0; cursor -= 1) {
    const item = items[cursor];
    if (movementType(item) === movementType(items[index]) && !isBackdownRow(item)) {
      const estimate = estimatedLoadWithContext(item, cursor, items);
      const numeric = numberFrom(estimate || item.weight);
      if (numeric) return numeric;
    }
  }
  return 0;
}

function backdownDropFactor(item, index, items) {
  if (!isTestWeekIndex()) return 0.9;
  const day = currentDay().day.toLowerCase();
  const type = movementType(item);
  if (day.includes("tue") && type === "deadlift") return 0.875;
  return 0.925;
}

function generatedBackdownRows(item, index, log, items) {
  const goal = backdownGoal(item);
  const prescription = parsedSetPrescription(item, index, log);
  const remaining = Math.max(0, goal - prescription.sets);
  const reps = prescription.reps || item.reps || "";
  const topLoad = topSetLoadBefore(items, index);
  const source = items[index - 1] || item;
  const baseBackdown = topLoad ? roundLoad(topLoad * backdownDropFactor(item, index, items)) : 0;
  const secondDrop = baseBackdown ? roundLoad(baseBackdown * 0.94) : 0;
  const baseLoad = baseBackdown ? `${baseBackdown} kg` : "降 8-12%";
  const reducedLoad = secondDrop ? `${secondDrop} kg` : "再降 5-10%";
  const rows = [];
  if (prescription.sets > 0) {
    rows.push({ status: "done", sets: prescription.sets, reps, rpe: source.rpe || "-", load: baseLoad });
  }
  if (remaining > 0) {
    rows.push({
      status: prescription.sets > 0 ? "reduced" : "remaining",
      sets: remaining,
      reps,
      rpe: source.rpe || "-",
      load: prescription.sets > 0 ? reducedLoad : baseLoad,
    });
  }
  return rows;
}

function isBackdownRow(item) {
  return String(item.sets) === "0" && hasAutoWeightNote(item);
}

